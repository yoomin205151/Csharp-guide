Métodos de filtrado:

Where: Filtra elementos según una condición.
OfType: Filtra elementos de un tipo específico.
Take: Selecciona un número específico de elementos desde el principio.
Skip: Omite un número específico de elementos desde el principio.
TakeWhile: Toma elementos mientras una condición sea verdadera.
SkipWhile: Omite elementos mientras una condición sea verdadera.
Distinct: Retorna elementos únicos de la colección.
Except: Retorna elementos que no están en otra colección.
Intersect: Retorna elementos presentes en ambas colecciones.
Métodos de proyección:

Select: Proyecta cada elemento a un nuevo tipo.
SelectMany: Proyecta y aplana una colección de colecciones.
Cast: Convierte elementos a un tipo específico.
OfType: Filtra y convierte elementos a un tipo específico.
Métodos de ordenamiento:

OrderBy: Ordena elementos en orden ascendente.
OrderByDescending: Ordena elementos en orden descendente.
ThenBy: Ordena elementos secundariamente después de una ordenación previa.
ThenByDescending: Ordena elementos secundariamente en orden descendente.
Métodos de agrupación:

GroupBy: Agrupa elementos según una clave.
ToLookup: Agrupa elementos en un objeto Lookup.
Métodos de agregación:

Count: Cuenta el número de elementos en la colección.
Sum: Calcula la suma de valores numéricos.
Average: Calcula el promedio de valores numéricos.
Min: Encuentra el valor mínimo.
Max: Encuentra el valor máximo.
Aggregate: Realiza una agregación personalizada.
Métodos de unión:

Join: Realiza una unión entre dos colecciones.
GroupJoin: Realiza una unión entre dos colecciones y agrupa los resultados.
Métodos de conversión:

ToList: Convierte una consulta en una lista.
ToDictionary: Convierte una consulta en un diccionario.
ToArray: Convierte una consulta en un arreglo.
ToLookup: Convierte una consulta en un objeto Lookup.
Otros métodos:

Concat: Combina dos colecciones.
Reverse: Invierte el orden de los elementos en la colección.
Zip: Combina dos colecciones elemento por elemento.
SequenceEqual: Comprueba si dos colecciones son iguales.